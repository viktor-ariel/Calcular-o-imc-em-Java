    private void calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularActionPerformed
        peso = Float.parseFloat(p.getText());
        altura = Float.parseFloat(a.getText());
        imc = peso / (altura * altura); 
        String mostrarResult = String.valueOf(imc);
        result.setText(mostrarResult);
        
        String sx = mouf.getSelectedItem().toString();
        sss.setText(sx); 
         
        if (s == true){
            //Homem
            if (imc < 20) { 
                statusResult.setText("Abaixo do normal!");
            } 
            else if (imc < 24.9 && imc >= 20) {
                statusResult.setText("Normal");
            }
            else if (imc < 29.9 && imc >= 25) {
                statusResult.setText("Obesidade Level");
            }
            else if (imc < 39.9 && imc >= 30) {
                statusResult.setText("Obesidade Moderada!");
            }
            else {
                statusResult.setText("Obesidade MÃ³rbida!");
            }
        }
        else{
            //Mulher
             if (imc < 19) { 
                statusResult.setText("Abaixo do normal!");
            } 
            else if (imc < 23.9 && imc >= 19) {
                statusResult.setText("Normal");
            }
            else if (imc < 28.9 && imc >= 24) {
                statusResult.setText("Obesidade Level");
            }
            else if (imc < 38.9 && imc >= 29) {
                statusResult.setText("Obesidade Moderada!");
            }
            else {
                statusResult.setText("Obesidade MÃ³rbida!");
            }
        }
    }//GEN-LAST:event_calcularActionPerformed

